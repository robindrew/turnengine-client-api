package com.turnengine.client.api.global.game;

import com.robindrew.codegenerator.api.serializer.xml.IXmlReader;
import com.robindrew.codegenerator.api.serializer.xml.IXmlSerializer;
import com.robindrew.codegenerator.api.serializer.xml.IXmlWriter;
import com.robindrew.codegenerator.api.serializer.xml.serializer.lang.StringSerializer;

public class SetGameInstancePropertyXmlSerializer implements IXmlSerializer<ISetGameInstanceProperty> {

	private String name;

	public SetGameInstancePropertyXmlSerializer() {
		this("SetGameInstanceProperty");
	}

	public SetGameInstancePropertyXmlSerializer(String name) {
		this.name = name;
	}

	/**
	 * Getter for the name field.
	 * @return the value of the name field.
	 */
	public String getName() {
		return name;
	}

	@Override
	public ISetGameInstanceProperty readObject(IXmlReader reader) {
		reader.startElement(getName());
		long param1 = reader.readLong("loginId");
		int param2 = reader.readInt("instanceId");
		String param3 = reader.readObject(new StringSerializer("key"));
		String param4 = reader.readObject(new StringSerializer("value"));
		reader.endElement(getName());

		// Create the bean
		return new SetGameInstanceProperty(param1, param2, param3, param4);
	}

	@Override
	public void writeObject(IXmlWriter writer, ISetGameInstanceProperty object) {
		writer.startElement(getName());
		writer.writeLong("loginId", object.getLoginId());
		writer.writeInt("instanceId", object.getInstanceId());
		writer.writeObject(object.getKey(), new StringSerializer("key"));
		writer.writeObject(object.getValue(), new StringSerializer("value"));
		writer.endElement(getName());
	}
}
